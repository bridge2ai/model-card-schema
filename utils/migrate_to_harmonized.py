def migrate_model_card(input_data: Dict[str, Any], datasheet_output_dir: Path) -> Dict[str, Any]:
    """
    Migrate a model card to use datasheet references.

    Returns the migrated model card data.
    """
    output_data = input_data.copy()

    # 1. Handle language → model_language migration
    if 'language' in output_data:
        output_data['model_language'] = output_data.pop('language')
        print("✓ Migrated 'language' → 'model_language'")

    # 2. Handle model_parameters.data → dataset references
    if 'model_parameters' in output_data:
        params = output_data['model_parameters']

        if 'data' in params:
            datasets = params['data']
            if not isinstance(datasets, list):
                datasets = [datasets]

            # Create datasheet stubs
            datasheet_refs = []
            for dataset in datasets:
                if isinstance(dataset, dict):
                    datasheet_ref = create_datasheet_stub(dataset, datasheet_output_dir)
                    datasheet_refs.append(datasheet_ref)

            # Add dataset_documentation section
            if 'dataset_documentation' not in output_data:
                output_data['dataset_documentation'] = {}

            output_data['dataset_documentation']['training_datasets'] = datasheet_refs

            # Keep original data for backward compatibility
            # Add note about datasheet reference
            for dataset in datasets:
                if isinstance(dataset, dict):
                    dataset['datasheet_info'] = (
                        "Complete dataset documentation available as separate Datasheet. "
                        "See dataset_documentation section for references."
                    )

            print(f"✓ Created {len(datasheet_refs)} datasheet reference(s)")

    # 3. Add migration metadata
    if 'migration_info' not in output_data:
        output_data['migration_info'] = {
            'migrated_on': datetime.now().isoformat(),
            'migration_tool': 'migrate_to_harmonized.py v1.0',
            'migration_pattern': 'Pattern 1: External References',
            'notes': (
                'This model card has been migrated to reference external Datasheets for Datasets. '
                'Complete the stub datasheet files in the datasheets/ directory.'
            )
        }

    return output_data


def main():
    """Main migration function."""
    if len(sys.argv) < 3:
        print("Usage: python utils/migrate_to_harmonized.py input.yaml output.yaml")
        print()
        print("This tool migrates model cards to use Datasheets for Datasets references.")
        print("It will:")
        print("  1. Convert 'language' → 'model_language'")
        print("  2. Create stub datasheet files for each dataset")
        print("  3. Add dataset_documentation section with datasheet references")
        print("  4. Preserve backward compatibility")
        print()
        print("Output:")
        print("  - Migrated model card: output.yaml")
        print("  - Stub datasheets: datasheets/*.yaml (needs completion)")
        sys.exit(1)

    input_file = sys.argv[1]
    output_file = sys.argv[2]

    print(f"Model Card Migration Tool")
    print(f"=" * 60)
    print(f"Input:  {input_file}")
    print(f"Output: {output_file}")
    print()

    # Create datasheets directory
    datasheet_dir = Path('datasheets')
    datasheet_dir.mkdir(exist_ok=True)

    # Load input
    print("Loading input model card...")
    try:
        input_data = load_yaml(input_file)
    except Exception as e:
        print(f"❌ Error loading input file: {e}")
        sys.exit(1)

    # Migrate
    print("\nMigrating model card...")
    try:
        output_data = migrate_model_card(input_data, datasheet_dir)
    except Exception as e:
        print(f"❌ Error during migration: {e}")
        import traceback
        traceback.print_exc()
        sys.exit(1)

    # Save output
    print(f"\nSaving migrated model card to {output_file}...")
    try:
        save_yaml(output_data, output_file)
    except Exception as e:
        print(f"❌ Error saving output file: {e}")
        sys.exit(1)

    print("\n" + "=" * 60)
    print("✅ Migration completed successfully!")
    print()
    print("Next steps:")
    print("  1. Review the migrated model card:", output_file)
    print("  2. Complete the stub datasheet files in datasheets/")
    print("  3. Update datasheet URLs in dataset_documentation section")
    print("  4. Validate the migrated model card")
    print()
    print("See INTEGRATION_GUIDE.md for more information.")


if __name__ == '__main__':
    main()
