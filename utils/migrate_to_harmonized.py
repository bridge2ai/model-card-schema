#!/usr/bin/env python3
"""
Migration utility for converting Model Cards to use Datasheets references.

This script helps migrate from simple dataSet references to external Datasheets
for Datasets references (Pattern 1 integration).

Usage:
    python utils/migrate_to_harmonized.py input.yaml output.yaml

Features:
    - Converts simple dataSet entries to datasheet references
    - Generates stub datasheet files for each dataset
    - Updates model_language (from language)
    - Preserves all other model card content
    - Creates backward-compatible output
"""

import sys
import yaml
from pathlib import Path
from typing import Dict, Any, List
from datetime import datetime


def load_yaml(file_path: str) -> Dict[str, Any]:
    """Load YAML file."""
    with open(file_path, 'r') as f:
        return yaml.safe_load(f)


def save_yaml(data: Dict[str, Any], file_path: str):
    """Save YAML file with nice formatting."""
    with open(file_path, 'w') as f:
        yaml.dump(data, f, default_flow_style=False, sort_keys=False, allow_unicode=True)


def generate_dataset_id(dataset_name: str) -> str:
    """Generate a dataset ID from name."""
    # Convert to lowercase, replace spaces/special chars with hyphens
    return dataset_name.lower().replace(' ', '-').replace('_', '-') + '-v1'


